package com.campaingup.memu.object;

public class Device {

    private String guid;
    private String time;

    public Device() {
        this.guid = guid;
        this.time = time;
    }

    public Device(String guid, String time) {
        this.guid = guid;
        this.time = time;
    }

    public String getGuid() {
        return guid;
    }

    public String getTime() {
        return time;
    }
}
